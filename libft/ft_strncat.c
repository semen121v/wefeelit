#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t		i;
	size_t		j;
	char		*src;

	src = (char*)s2;
	i = 0;
	j = 0;
	while (s1[i] != '\0')
		i++;
	while (src[j] != '\0' && n != 0)
	{
		s1[i] = src[j];
		i++;
		j++;
		n--;
	}
	s1[i] = '\0';
	return (s1);
}
