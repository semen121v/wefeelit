#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int		i;
	int		j;
	char	*src;

	src = (char*)s2;
	i = 0;
	j = 0;
	while (s1[i] != '\0')
		i++;
	while (src[j] != '\0')
	{
		s1[i] = src[j];
		i++;
		j++;
	}
	s1[i] = '\0';
	return (s1);
}
